/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode deleteDuplicates(ListNode head) {
        if(head == null) return null;
        ListNode tmp = head;
        while(tmp.next != null) {
            if(tmp.next.val == tmp.val) tmp.next = tmp.next == null ? null : tmp.next.next;
            else tmp = tmp.next;
        }
        return head;
    }
}
